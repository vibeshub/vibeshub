def _write(path: Path, text: str, *, executable: bool = False) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")
    if executable:
        mode = path.stat().st_mode
        path.chmod(mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)


def _clean_managed(out: Path) -> None:
    for name in _MANAGED:
        target = out / name
        if target.is_dir() and not target.is_symlink():
            shutil.rmtree(target)
        elif target.exists() or target.is_symlink():
            target.unlink()


def generate(out: Path, source: Path = _DEFAULT_SOURCE) -> Path:
    """Generate the Cursor plugin tree into `out` from plugin `source`.

    Only the managed paths are removed/written; anything else in `out` (e.g. a
    `.git` directory when `out` is a real clone) is left untouched. Returns `out`.
    """
    out = Path(out)
    source = Path(source)
    version = _read_version(source)

    out.mkdir(parents=True, exist_ok=True)
    _clean_managed(out)

    # Manifests.
    _write(out / ".cursor-plugin" / "marketplace.json", _marketplace_json())
    _write(out / ".cursor-plugin" / "plugin.json", _plugin_json(version))

    # Hooks: Cursor-format manifest, generated wrapper, copied share script.
    _write(out / "hooks" / "hooks.json", _hooks_json())
    _write(out / "hooks" / "on-pr-share.sh", _WRAPPER, executable=True)
    shutil.copy2(source / "hooks" / "on-pr-share.py", out / "hooks" / "on-pr-share.py")

    # Runtime code: the whole vibeshub_client/ package plus the top-level modules.
    shutil.copytree(
        source / "vibeshub_client",
        out / "vibeshub_client",
        ignore=shutil.ignore_patterns("__pycache__", "*.pyc"),
    )
    for module in _TOP_MODULES:
        shutil.copy2(source / module, out / module)

    # Docs / license.
    _write(out / "README.md", _readme(version))
    _write(out / "LICENSE", _license())

    return out


def check(out: Path, source: Path = _DEFAULT_SOURCE) -> bool:
    """Return True if `out` matches a fresh generate (no drift), else False."""
    out = Path(out)
    with tempfile.TemporaryDirectory() as tmp:
        fresh = generate(Path(tmp) / "vibeshub-cursor", source=source)
        return _trees_equal(fresh, out)


def _trees_equal(a: Path, b: Path) -> bool:
    """Compare only the managed paths of two trees, byte-for-byte."""
    for name in _MANAGED:
        pa, pb = a / name, b / name
        if pa.is_dir():
            if not _dirs_equal(pa, pb):
                return False
        else:
            if not pb.is_file() or pa.read_bytes() != pb.read_bytes():
                return False
    return True


def _dirs_equal(a: Path, b: Path) -> bool:
    cmp = filecmp.dircmp(a, b)
    if cmp.left_only or cmp.right_only or cmp.diff_files or cmp.funny_files:
        return False
    for sub in cmp.common_dirs:
        if not _dirs_equal(a / sub, b / sub):
            return False
    return True


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Generate the Cursor marketplace plugin from plugins/cli.")
    parser.add_argument("--out", type=Path, default=_DEFAULT_OUT, help="output directory")
    parser.add_argument("--source", type=Path, default=_DEFAULT_SOURCE, help="plugin source directory")
    parser.add_argument("--check", action="store_true", help="check --out is in sync; exit 1 on drift")
    args = parser.parse_args(argv)

    if args.check:
        if check(args.out, source=args.source):
            print(f"in sync: {args.out}")
            return 0
        print(f"OUT OF SYNC: {args.out} differs from a fresh generate", file=sys.stderr)
        return 1

    out = generate(args.out, source=args.source)
    print(f"Generated Cursor plugin at {out}")
    print("Local test: symlink into ~/.cursor/plugins/local/ then Reload Window in Cursor.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
